def get_random_number_from_website():
    response = requests.get('https://www.math-only-math.com/Whole-Numbers.html')
    if response.status_code == 200:
        soup = BeautifulSoup(response.content, 'html.parser')
        number_elements = soup.find_all('li')
        if number_elements:
            random_number = random.choice(number_elements).text
            return random_number
    return None

