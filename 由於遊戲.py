    shapes = ["圓形", "三角形", "星星", "雨傘"]
    selected_shape = None

    while selected_shape is None:
        shape_input = input("請輸入選擇的形狀代號（1:圓形 2:三角形 3:星星 4:雨傘）：")
        if shape_input.isdigit():
            shape_index = int(shape_input) - 1
            if 0 <= shape_index < len(shapes):
                selected_shape = shapes[shape_index]
            else:
                print("無效的選擇，請重新選擇。")
        else:
            print("無效的選擇，請重新選擇。")

    print(f"您選擇了 {selected_shape} 形狀。")

    total = 0

    for i in range(5):
        print(f"請問您在第 {i+1} 次時，要如何分離椪糖呢？")
        print("1: 滴汗融化/2: 針戳圖案/3: 舌頭舔舐")

        valid_choice = False
        while not valid_choice:
            method_input = input("請輸入選擇方式：")

            if method_input == "1":
                total += 3
                valid_choice = True
            elif method_input == "2":
                total += random.randint(1, 2)
                valid_choice = True
            elif method_input == "3":
                total += int(method_input)
                valid_choice = True
            else:
                print("無效的選擇，請重新選擇。")

        reply_delay = random.randint(0, 5)
        time.sleep(reply_delay)

    if total % 2 == 0:
        print("很抱歉，遊戲失敗！您被淘汰了！")
        return False
    else:
        print("恭喜！通關成功！")
        return True

